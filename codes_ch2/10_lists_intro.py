# An empty list. We add elements one-by-one
# In this case we add tuples
le = []
le.append((2015, 3, 14))
le.append((2015, 4, 18))
print(le)

# We can also explicitly assign values during creation
l = [1, 'string', 20.5, (23, 45)]
print(l)

# We can retrieve an element using their index
print(l[1])
